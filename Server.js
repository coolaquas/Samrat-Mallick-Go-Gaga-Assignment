var express = require("express");
const mongoose = require("mongoose");
var apiRouter = require("./Routes/index");
require("dotenv").config();
var cors = require("cors");

//API configuration
//=========================================================================================================================================
const app = express();
const port = process.env.PORT || 8001;
//=========================================================================================================================================

//middleWare
//=========================================================================================================================================
app.use(cors());
app.use((req, res, next) => {
  console.log(
    `${req.protocol}://${req.get("host")}${req.url} -- ${new Date()}`
  );
  next();
});
app.use(express.json());
app.use("/api", apiRouter);

//=========================================================================================================================================

//Db Configuration
//=========================================================================================================================================
const connection_url = `mongodb+srv://${process.env.USER_Name}:${process.env.Password}@cluster0.zhy9a.mongodb.net/Web-app?retryWrites=true&w=majority`;
mongoose.Promise = global.Promise;
mongoose
  .connect(connection_url, {
    useNewUrlParser: true,
    useUnifiedTopology: true,
  })
  .then(() => console.log("DataBase Connection successful"))
  .catch((err) => console.log(err));
//=========================================================================================================================================

//API Endpoint
//=========================================================================================================================================
app.get("/", (req, res) => {
  res.status(200).send("Sanity Check ok!!!");
});
//=========================================================================================================================================

//Listner
//=========================================================================================================================================
app.listen(port, () => {
  console.log(`Listning on Port No. : ${port}`);
});
//=========================================================================================================================================
